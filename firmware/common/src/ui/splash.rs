/// Splash screen
#[must_use]
pub struct Splash;

impl UiContent for Splash {
    #[cfg(debug_assertions)]
    const FOOTER_LEFT: &str = "(DEBUG)";
    const FOOTER_RIGHT: &str = GIT_SHA_STR;

    fn draw_content<D: DrawTarget<Color = BinaryColor>>(
        &self,
        target: &mut D,
    ) -> Result<(), D::Error> {
        let logo = Image::new(&LOGO, Point::zero());

        let version_text = format!("v{VERSION_STR}");
        let version = Text::new(&version_text, Point::zero(), VERSION_STYLE);

        LinearLayout::vertical(Chain::new(logo).append(version))
            .with_alignment(horizontal::Center)
            .with_spacing(spacing::FixedMargin(4))
            .arrange()
            .align_to(&target.bounding_box(), horizontal::Center, vertical::Center)
            .draw(target)?;

        Ok(())
    }
}

impl UiInteraction for Splash {
    type Input = ();
    const TIMEOUT: Option<Duration> = Some(TIMEOUT);

    async fn read_input<D: DeviceTypes>(
        &mut self,
        frontend: &mut Frontend<'_, '_, D>,
    ) -> Result<Self::Input, Error<D>> {
        info!("UI: Displaying splash screen");

        // Output startup/testing tone
        frontend.buzzer.startup().await;

        // Wait for any key to be pressed
        let _key = frontend.keypad.read().await.map_err(Error::Keypad)?;
        Ok(())
    }
}
